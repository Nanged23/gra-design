<script>
export default {
  data() {
    return {
      active: 'home',
      active1: 0,
      stickMenu: false,
      activeSidebar: false,
      currentDateTime: '',
    }
  },
  methods: {
    toggleSidebar(event) {
      event.stopPropagation();
      this.activeSidebar = !this.activeSidebar;
      if (!this.activeSidebar) {
        // 关闭菜单时重置固定状态
        this.stickMenu = false;
      }
    },
    handleOutsideClick(event) {
      // 检查点击是否在菜单栏外
      if (!this.$refs.sidebar || !this.$refs.sidebar.$el) {
        return;
      }
      const sidebar = this.$refs.sidebar.$el;
      const menuButton = document.querySelector('.bx-menu');
      if (this.stickMenu) {
        return;
      }

      const isClickOutside = !sidebar.contains(event.target) && event.target !== menuButton &&
        !menuButton?.contains(event.target);
      if (isClickOutside && this.activeSidebar) {
        this.activeSidebar = false;
      }
    },
    goToHome() {
      this.$router.push('/indexPage');
    },
    updateTime() {
      const now = new Date();
      const year = now.getFullYear();
      const month = (now.getMonth() + 1).toString().padStart(2, '0');
      const day = now.getDate().toString().padStart(2, '0');
      const hours = now.getHours().toString().padStart(2, '0');
      const minutes = now.getMinutes().toString().padStart(2, '0');
      this.currentDateTime = `${year}-${month}-${day} ${hours}:${minutes}`;
    }
  },
  mounted() {
    this.$nextTick(() => {
      document.addEventListener('click', this.handleOutsideClick);
      // 初始化时间
      this.updateTime();
      // 每分钟更新一次时间
      setInterval(this.updateTime, 60000);
    });
  },
  beforeDestroy() {
    document.removeEventListener('click', this.handleOutsideClick);
    // 清除定时器
    if (this.timer) {
      clearInterval(this.timer);
    }
  }
}
